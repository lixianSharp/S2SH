package cn.itcast.f_extends2;
//动物父类  只定义了id和姓名
public class Animal {
	private int id; //id
	private String name;//姓名
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
