package cn.itcast.e_component;
//车轮
public class Wheel {
	//车轮个数
	private int count;
	//尺寸
	private double size;
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public double getSize() {
		return size;
	}
	public void setSize(double size) {
		this.size = size;
	}
}
